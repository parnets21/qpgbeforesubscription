const mongoose = require('mongoose');

const resultMakerSubjectSchema = new mongoose.Schema({
  subjectName: {
    type: String,
    required: true
  },
  classId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Class',
    required: true
  },
  userId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Teacher',
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

module.exports = mongoose.model('ResultMakerSubject', resultMakerSubjectSchema);
